    private void cbclient1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cbclient1MousePressed
        // TODO add your handling code here:
        cbclient1.removeAllItems();
                    Transaction tr=null;
                    Session sess=ConnectHibernateUtil.getSessionFactory().openSession();
                    tr=sess.beginTransaction();
                    SQLQuery query=sess.createSQLQuery("select fname from Client");
                    List ls=query.list();
                    for(Object boca:ls)
                    {
                    cbclient1.addItem((String)boca);
                    }
    }//GEN-LAST:event_cbclient1MousePressed

