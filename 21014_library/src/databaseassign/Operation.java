    private void populateTable() {
           Checkincon c=new Checkincon();
             List<Checkin>b=c.listAll();
    DefaultTableModel model=(DefaultTableModel)checkintable.getModel();
    model.setRowCount(0);
    for(Checkin rs:b){
       Object[] obj=new Object[]{rs.getDate(),rs.getClient(),rs.getBook(),rs.getStatus()};
       model.addRow(obj);
    }
           
          
    }
