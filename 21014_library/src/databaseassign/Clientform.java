      public void populateTable(){
           
           CLientcon c=new CLientcon();
             List<Client>b=c.listAll();
   DefaultTableModel model=(DefaultTableModel)clienttable.getModel();
    model.setRowCount(0);
    for(Client cli:b){
       Object[] obj=new Object[]{cli.getId(),cli.getFname(),cli.getLname(),cli.getPhone(),cli.getEmail(),cli.getClientcat(),cli.getImage()};
       model.addRow(obj);
}
}
