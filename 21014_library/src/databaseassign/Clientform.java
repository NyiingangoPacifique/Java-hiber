    private void btnuploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnuploadActionPerformed
        // TODO add your handling code here:
        JFileChooser file=new JFileChooser();
        FileNameExtensionFilter fphoto=new FileNameExtensionFilter("IMAGES","png","jpg","jfif");
        file.addChoosableFileFilter(fphoto);
        if(file.showOpenDialog(null)==JFileChooser.APPROVE_OPTION){
        File cfile=file.getSelectedFile();
        FilePath=cfile.getAbsolutePath();
        ImageIcon img=new ImageIcon(FilePath);
        Image image=img.getImage().getScaledInstance(imglabel.getWidth(), imglabel.getHeight(), Image.SCALE_SMOOTH);
        imglabel.setIcon(new ImageIcon(image));
        }
    }//GEN-LAST:event_btnuploadActionPerformed

