    private void populateTable() {
        Bookcon c=new Bookcon();
             List<Book>b=c.listAll();
    DefaultTableModel model=(DefaultTableModel)booktable.getModel();
    model.setRowCount(0);
    for(Book cli:b){
       Object[] obj=new Object[]{cli.getBookid(),cli.getTitle(),cli.getPublishing(),cli.getDateofpub(),cli.getAuthor(),cli.getPages(),cli.getBookcate()};
       model.addRow(obj);
    }
    }

    private void populateTable2() {
          Bookcatcon c=new Bookcatcon();
             List<Boocategory>b=c.listAll();
    model=(DefaultTableModel)bookcategorytable.getModel();
    model.setRowCount(0);
    for(Boocategory bc:b){
       Object[] obj=new Object[]{bc.getCategoryid(),bc.getCatename()};
       model.addRow(obj);
    }
    }
      
    

