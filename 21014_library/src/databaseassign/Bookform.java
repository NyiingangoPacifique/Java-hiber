    private void cbcategoryMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cbcategoryMousePressed
        // TODO add your handling code here:
                    cbcategory.removeAllItems();
                    Transaction tr=null;
                    Session sess=ConnectHibernateUtil.getSessionFactory().openSession();
                    tr=sess.beginTransaction();
                    SQLQuery query=sess.createSQLQuery("select catename from Boocategory");
                    List ls=query.list();
                    for(Object boca:ls)
                    {
                    cbcategory.addItem((String)boca);
                    }
    }//GEN-LAST:event_cbcategoryMousePressed

